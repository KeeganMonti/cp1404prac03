import random
print(random.randint(5, 20))  # line 1, can be a random whole number from 5 to 20 both inclusive.
print(random.randrange(3, 10, 2))  # line 2, prints an number between 3 and 10 inclusive, however has 2 spacing
# between selecting numbers so can only select: 3,5,7,9.
print(random.uniform(2.5, 5.5))  # line 3, prints a random decimal between 2.5 and 5.5 inclusive.

print(random.randint(1, 100))
